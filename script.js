        input.type = inputType;
        input.name = groupName;
        input.value = optIdx;

        const saved = userAnswers[currentIndex];
        if (q.type === 'checkbox' && saved && Array.isArray(saved) && saved.includes(optIdx)) {
            input.checked = true;
        } else if (q.type === 'radio' && saved !== null && saved === optIdx) {
            input.checked = true;
        }

