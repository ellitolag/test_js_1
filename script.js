function navigate(delta) {
    saveCurrentAnswer();
    const newIndex = currentIndex + delta;
    if (newIndex >= 0 && newIndex < questionsData.length) {
        currentIndex = newIndex;
        renderCurrentQuestion();
        updateStatsAndProgress();
        highlightCurrentInNav();
    }
}

function skipCurrentQuestion() {
    skippedQuestions.add(currentIndex);
    saveCurrentAnswer();
    if (currentIndex + 1 < questionsData.length) {
        currentIndex++;
        renderCurrentQuestion();
    } else {
        alert("Это последний вопрос. Вы можете завершить тест или вернуться к пропущенным.");
    }
    updateStatsAndProgress();
    buildNavGrid();
}

function saveCurrentAnswer() {
    const currentAnswer = getCurrentAnswer();
    if (currentAnswer !== null) {
        userAnswers[currentIndex] = currentAnswer;
    }
}

