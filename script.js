// Функция проверки правильности ответа
function isAnswerCorrect(questionIndex, answer) {
    const q = questionsData[questionIndex];
    if (answer === null) return null;

    if (q.type === 'radio') {
        return answer === q.correct[0];
    } else if (q.type === 'checkbox') {
        if (!Array.isArray(answer) || answer.length === 0) return null;
        const isFullyCorrect = answer.length === q.correct.length && q.correct.every(c => answer.includes(c));
        if (isFullyCorrect) return 'full';
        const hasSomeCorrect = answer.some(a => q.correct.includes(a));
        const hasIncorrect = answer.some(a => !q.correct.includes(a));
        if (hasSomeCorrect && !hasIncorrect) return 'partial';
        if (hasIncorrect) return 'incorrect';
        return null;
    } else if (q.type === 'matching') {
        if (!Array.isArray(answer) || answer.length !== q.correct.length) return null;
        let correctCount = 0;
        for (let i = 0; i < answer.length; i++) {
            if (answer[i] === q.correct[i]) correctCount++;
        }
        if (correctCount === q.correct.length) return 'full';
        if (correctCount > 0) return 'partial';
        return 'incorrect';
    }
    return null;
}

// Получение цвета для кнопки навигации
function getNavButtonClass(questionIndex) {
    const answer = userAnswers[questionIndex];
    if (answer === null) return 'unanswered';

    const q = questionsData[questionIndex];
    if (q.type === 'radio') {
        const isCorrect = answer === q.correct[0];
        return isCorrect ? 'correct' : 'incorrect';
    } else if (q.type === 'checkbox') {
        if (!Array.isArray(answer) || answer.length === 0) return 'unanswered';
        const isFullyCorrect = answer.length === q.correct.length && q.correct.every(c => answer.includes(c));
        if (isFullyCorrect) return 'correct';
        const hasSomeCorrect = answer.some(a => q.correct.includes(a));
        const hasIncorrect = answer.some(a => !q.correct.includes(a));
        if (hasSomeCorrect && !hasIncorrect) return 'partial';
        if (hasIncorrect) return 'incorrect';
        return 'unanswered';
    } else if (q.type === 'matching') {
        if (!Array.isArray(answer) || answer.length !== q.correct.length) return 'unanswered';
        let correctCount = 0;
        for (let i = 0; i < answer.length; i++) {
            if (answer[i] === q.correct[i]) correctCount++;
        }
        if (correctCount === q.correct.length) return 'correct';
        if (correctCount > 0) return 'partial';
        return 'incorrect';
    }
    return 'unanswered';
}

// Получение текущего выбранного ответа
function getCurrentAnswer() {
    const currentQ = questionsData[currentIndex];
    if (currentQ.type === 'radio') {
        const selected = document.querySelector('input[name="current_q"]:checked');
        return selected ? parseInt(selected.value) : null;
    } else if (currentQ.type === 'checkbox') {
        const checkboxes = document.querySelectorAll('input[name="current_q"]');
        const selectedValues = [];
        checkboxes.forEach(cb => {
            if (cb.checked) selectedValues.push(parseInt(cb.value));
        });
        return selectedValues.length > 0 ? selectedValues : null;
    } else if (currentQ.type === 'matching') {
        const selects = document.querySelectorAll('.matching-select');
        if (selects.length === 0) return null;
        const selectedValues = [];
        selects.forEach(select => {
            const val = parseInt(select.value);
            selectedValues.push(isNaN(val) ? null : val);
        });
        return selectedValues;
    }
    return null;
}

// Функция проверки текущего ответа
function checkCurrentAnswer() {
    const currentQ = questionsData[currentIndex];
    const currentAnswer = getCurrentAnswer();

    if (currentAnswer === null || (Array.isArray(currentAnswer) && currentAnswer.length === 0)) {
        questionStatusDiv.innerHTML = '<span class="status-warning">⚠ Сначала выберите ответ!</span>';
        return;
    }

    // Для matching проверяем, что все select выбраны
    if (currentQ.type === 'matching') {
        const hasEmpty = currentAnswer.some(v => v === null);
        if (hasEmpty) {
            questionStatusDiv.innerHTML = '<span class="status-warning">⚠ Выберите название для каждой схемы!</span>';
            return;
        }
    }

    const correctness = isAnswerCorrect(currentIndex, currentAnswer);
    checkedStatus[currentIndex] = true;

    if (correctness === 'full' || correctness === true) {
        if (currentQ.type === 'matching') {
            questionStatusDiv.innerHTML = '<span class="status-correct">✓ Правильно! Все схемы сопоставлены верно!</span>';
        } else {
            questionStatusDiv.innerHTML = '<span class="status-correct">✓ Правильно! Отличный ответ!</span>';
        }
        const checkBtnElement = document.getElementById('checkBtn');
        if (checkBtnElement) {
            checkBtnElement.style.background = 'linear-gradient(135deg, #059669, #10b981)';
            setTimeout(() => {
                checkBtnElement.style.background = '';
            }, 500);
        }
    } else if (correctness === 'partial') {
        if (currentQ.type === 'matching') {
            const correctMatches = currentQ.correct;
            let resultHtml = '<span class="status-partial">⚠ Частично верно! Правильные соответствия:<br>';
            for (let i = 0; i < correctMatches.length; i++) {
                resultHtml += `&nbsp;&nbsp;• Схема ${i+1} → ${currentQ.matchingOptions[correctMatches[i]]}<br>`;
            }
            resultHtml += '</span>';
            questionStatusDiv.innerHTML = resultHtml;
        } else {
            const correctAnswers = currentQ.correct.map(c => String.fromCharCode(97 + c)).join(', ');
            questionStatusDiv.innerHTML = `<span class="status-partial">⚠ Частично верно! Правильные варианты: ${correctAnswers}</span>`;
        }
    } else if (correctness === 'incorrect' || correctness === false) {
        if (currentQ.type === 'matching') {
            const correctMatches = currentQ.correct;
            let resultHtml = '<span class="status-incorrect">✗ Неправильно! Правильные соответствия:<br>';
            for (let i = 0; i < correctMatches.length; i++) {
                resultHtml += `&nbsp;&nbsp;• Схема ${i+1} → ${currentQ.matchingOptions[correctMatches[i]]}<br>`;
            }
            resultHtml += '</span>';
            questionStatusDiv.innerHTML = resultHtml;
        } else {
            const correctAnswers = currentQ.correct.map(c => String.fromCharCode(97 + c)).join(', ');
            questionStatusDiv.innerHTML = `<span class="status-incorrect">✗ Неправильно! Правильный ответ: ${correctAnswers}</span>`;
        }
    }

    buildNavGrid();
    updateStatsAndProgress();
}

